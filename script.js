nomeE1 = prompt("Digite o nome de Estudante 1:")
notasE1 = prompt("Digite as 3 notas de Estudante 1:")
notasE1 = notasE1.split(",")

mediaE1 = ( parseFloat(notasE1[0]) + parseFloat(notasE1[1]) + parseFloat(notasE1[2]) ) / 3

if(mediaE1 >= 5){
    alert(nomeE1 + " obteve aprovação na média.")
}
else if(mediaE1 < 5){
    alert(nomeE1 + " não obteve aprovação na média.")
}

